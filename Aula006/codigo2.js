var n1 = Number(window.prompt('Digite um numero: ')) // o prompt converte qualquer valor para string
var n2 = Number(window.prompt('Digite outro numero: '))// é necessario colocar o Number() para converter
var s = n1 + n2
window.alert(`A soma dos valores é: ${s}`)
//(number + number) = adição
//(string + string) = concatenação